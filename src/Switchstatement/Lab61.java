package Switchstatement;

public class Lab61 {
    public static void main(String[] args) {
        int a =98;
        switch (a) {
            case 98:
                System.out.println("Matched value 98");
                break;
            case 99:
                System.out.println("Matched value 99");
                break;
            default:
                System.out.println("No match found for the value");
        }
    }
}
