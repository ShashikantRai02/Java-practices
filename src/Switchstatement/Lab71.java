package Switchstatement;

public class Lab71 {
    public static void main(String[] args) {
        char code= 'C';
        switch (code) {
            default:
                System.out.println("No matching grade found");
            case 'A':
                System.out.println("Grade 65");
                break;
                case 'B':
                System.out.println("Grade B");
                break;
            case 'D':
                System.out.println("Grade D");
                break;

        }
    }
}
