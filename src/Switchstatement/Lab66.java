package Switchstatement;

public class Lab66 {
    public static void main(String[] args) {
        String day = "MONDAY";
        switch (day) {
            case "MONDAY":
                System.out.println("It's Monday!");
                break;
            case "TUESDAY":
                System.out.println("It's Tuesday!");
                break;
            default:
                System.out.println("It's some other day!");
        }
    }
}
