package Arrays;

public class Lab108 {
    public static void main(String[] args) {
        int[] arr = {1, 2, 3, 4, 5};
        System.out.println("Length of the array: " + arr.length);
        System.out.println("First element: " + arr[0]);//arr[0] is used to access the first element of the array because array index starts from 0
        System.out.println("Last element: " + arr[arr.length - 1]);//arr.length-1 is used to access the last element of the array because array index starts from 0
    }
}
